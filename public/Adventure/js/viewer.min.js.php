<?php
	$setID = '';
	if(isset($_GET['set'])){
		$setID = (int)$_GET['set'] > 1 ? (int)$_GET['set'] : '';
	}
	include(str_replace(['viewer','.php'],['viewer'.$setID,''],$_SERVER['SCRIPT_FILENAME']));
?>
$(function(){
	Adventure.assetPath = '<?php echo preg_replace('/js\/[^\/]+$/','',$_SERVER['PHP_SELF']); ?>';
	var embedScript = $('script[data-adventure]');
	$("<div/>", {class: "adventure-start", style:"display:none;"}).html("<div><p>Begin Adventure</p></div>").insertAfter(embedScript).click(function(){
		$("<div/>", {id: "adventure", "data-adventure": embedScript.attr('data-adventure')}).appendTo("body");
		Adventure.viewer = new Adventure.Viewer();
	});
	$("<link/>", {rel: "stylesheet", type: "text/css", href: Adventure.assetPath+"css/AdventureViewer.css"}).appendTo("head").load(function(){
		$(".adventure-start").fadeIn();
	});
});